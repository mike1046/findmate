export const tumblrUrl = shareUrl => `https://www.tumblr.com/widgets/share/tool?canonicalUrl=${shareUrl}`;
export const redditUrl = shareUrl => `https://reddit.com/submit?url=${shareUrl}`;
export const facebookUrl = shareUrl => `https://www.facebook.com/sharer.php?u=${shareUrl}`;
export const twitterUrl = shareUrl => `https://twitter.com/intent/tweet?url=${shareUrl}`;
export const googleUrl = shareUrl => `https://plus.google.com/share?url=${shareUrl}&hl=en`;
